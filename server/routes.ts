/**
 * Resolve the union of permissionKeys for the actor's roles. Used by
 * `requirePermission()` middleware and the `/api/users/me/permissions`
 * endpoint that the client `usePermissions()` hook calls. Admin role is
 * treated as a superuser (gets every permission).
 */
async function resolveActorPermissions(actorId: string): Promise<{ user: { id: string; name: string; roles: string[] } | null; permissions: Set<string> }> {
  const user = await getStorage().getUser(actorId);
  if (!user) return { user: null, permissions: new Set() };
  const allRoles = await getStorage().listRoles();
  // Authoritative: derive roles from user_role_assignments so the assignment
  // mutations (assignRole/unassignRole) drive effective permissions and
  // cannot drift from the cached user.roles[] mirror.
  const assignments = await getStorage().listAssignments();
  const assignedRoleIds = new Set(assignments.filter(a => a.userId === actorId).map(a => a.roleId));
  const assignedRoleNames = allRoles.filter(r => assignedRoleIds.has(r.id)).map(r => r.name);
  // Fallback to the embedded user.roles[] / user.role mirror for legacy rows
  // that have no assignments yet (so the system stays usable pre-reconcile).
  const fallbackRoles = (user.roles as string[] | null)?.length ? (user.roles as string[]) : (user.role ? [user.role] : []);
  const userRoles = assignedRoleNames.length ? assignedRoleNames : fallbackRoles;
  const matched = allRoles.filter((r) => userRoles.includes(r.name));
  const isAdmin = userRoles.includes("admin");
  const perms = new Set<string>();
  for (const r of matched) for (const p of (r.permissionKeys ?? [])) perms.add(p);
  if (isAdmin) {
    // Admin is a superuser even if the row's permissionKeys drift.
    for (const r of allRoles) for (const p of (r.permissionKeys ?? [])) perms.add(p);
  }
  return { user: { id: user.id, name: user.name, roles: userRoles }, permissions: perms };
}

/**
 * Express middleware factory: gates a route on a specific permission key.
 * Resolves the acting user from `x-actor-user-id`, expands their roles
 * to the union of permissionKeys, and rejects with 403 when missing.
 *
 * Defence-in-depth: the client `usePermissions()` hook also hides or
 * disables matching buttons, but the server is the source of truth.
 */
function requirePermission(key: string) {
  return async (req: Request, res: Response, next: () => void) => {
    try {
      const id = getActorUserId(req);
      if (!id) return res.status(401).json({ error: "Not signed in." });
      const { user, permissions } = await resolveActorPermissions(id);
      if (!user) return res.status(403).json({ error: "Unknown actor — user not registered." });
      if (!permissions.has(key)) return res.status(403).json({ error: `Your role does not allow this action (requires '${key}').` });
      next();
    } catch (e) {
      const msg = e instanceof Error ? e.message : String(e);
      console.error("[requirePermission]", req.method, req.path, "->", msg);
      res.status(500).json({ error: "Permission check failed." });
    }
  };
}

// Build an actor descriptor for audit-log entries. Source of truth is the
// verified session-derived actor id (set by requireActor); the user's stored
// display name is also stashed on the request by requireActor so audit
// entries always reflect the verified user, not whatever the client sends.
// Strip credential material before returning user rows over the API. The
// `users` table now carries `passwordHash` (Task #48); it must never appear
// in any HTTP response. Apply to every list/detail/create/update payload.
function sanitizeUser<T extends Record<string, unknown>>(u: T): Omit<T, "passwordHash" | "passwordUpdatedAt"> {
  const { passwordHash: _ph, passwordUpdatedAt: _pu, ...rest } = u as T & { passwordHash?: unknown; passwordUpdatedAt?: unknown };
  return rest;
}

function actorFromReq(req: Request): { actorUserId?: string; actorName: string; ipAddress?: string } {
  const id = getActorUserId(req);
  const ip = req.ip || req.socket.remoteAddress || undefined;
  // Actor name is sourced exclusively from the verified session user record
  // (stamped on req by requireActor). Never trust client-supplied headers
  // or body fields for audit attribution.
  return {
    actorUserId: id ?? undefined,
    actorName: (req as Request & { actorName?: string }).actorName || "system",
    ipAddress: ip,
  };
}

