  // Returns 403 when actor is not the owner (defence-in-depth IDOR guard).
  function assertChatOwnership(session: { ownerUserId: string | null }, req: import("express").Request): true | { status: number; error: string } {
    const actorId = getActorUserId(req) ?? "";
    if (session.ownerUserId && actorId && session.ownerUserId !== actorId) {
      return { status: 403, error: "You do not have access to this chat session." };
    }
    return true;
  }

  app.get("/api/chat-sessions/:id", requireDb, requireActor, asyncHandler(async (req, res) => {
    const session = await getStorage().getChatSession(req.params.id);
    if (!session) return res.status(404).json({ error: "Chat session not found" });
    const guard = assertChatOwnership(session, req);
    if (guard !== true) return res.status(guard.status).json({ error: guard.error });
    let messages: unknown[] = [];
    try {
      const buf = await getObjectStore().getObject(session.storageKey);
      const parsed = JSON.parse(buf.toString("utf8"));
      messages = Array.isArray(parsed?.messages) ? parsed.messages : [];
    } catch (e) {
      console.error("[chat-sessions] read OBS failed:", e instanceof Error ? e.message : e);
    }
    res.json({ session, messages });
  }));

