function asyncHandler(fn: (req: Request, res: Response) => Promise<unknown>) {
  return (req: Request, res: Response) => {
    fn(req, res).catch((e) => {
      const msg = e instanceof Error ? e.message : String(e);
      const cause = (e as { cause?: unknown })?.cause;
      const causeMsg = cause instanceof Error ? cause.message : (cause ? String(cause) : "");
      console.error("[api]", req.method, req.path, "->", msg, causeMsg ? `\n  cause: ${causeMsg}` : "");
      if (!res.headersSent) {
        // Only echo the underlying DB cause to clients in non-production so
        // production deployments don't leak schema/SQL details.
        const body: { error: string; cause?: string } = { error: msg };
        if (causeMsg && process.env.NODE_ENV !== "production") body.cause = causeMsg;
        res.status(500).json(body);
      }
    });
  };
}

// Authn gate: resolves the acting user from the verified session cookie
